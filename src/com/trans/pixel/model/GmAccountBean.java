package com.trans.pixel.model;

public class GmAccountBean {
	public String account = "";
	public int canreward = 0;
	public int canview = 0;
	public int canwrite = 0;
	public int master = 0;
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public int getCanreward() {
		return canreward;
	}
	public void setCanreward(int canreward) {
		this.canreward = canreward;
	}
	public int getCanview() {
		return canview;
	}
	public void setCanview(int canview) {
		this.canview = canview;
	}
	public int getCanwrite() {
		return canwrite;
	}
	public void setCanwrite(int canwrite) {
		this.canwrite = canwrite;
	}
	public int getMaster() {
		return master;
	}
	public void setMaster(int master) {
		this.master = master;
	}
}
